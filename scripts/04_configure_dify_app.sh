#!/bin/bash
#================================================================
# 04_configure_dify_app.sh
# 配置 Dify 应用：添加 Ollama 模型提供商 + 创建面部健康分析应用
# 使用方法: bash 04_configure_dify_app.sh
#================================================================

set -e

PROJECT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
source "$PROJECT_DIR/configs/env.conf" 2>/dev/null || true

DIFY_API_PORT="${DIFY_API_PORT:-5001}"
DIFY_WEB_PORT="${DIFY_WEB_PORT:-3000}"
OLLAMA_MODEL="${OLLAMA_MODEL:-qwen2.5-vl:7b}"
DIFY_API_URL="http://localhost:${DIFY_API_PORT}"

echo "=========================================="
echo "  Dify 应用配置指南"
echo "=========================================="
echo ""
echo "由于 Dify 的模型提供商和应用需要通过 Web UI 配置，"
echo "以下是详细的步骤说明。"
echo ""
echo "=========================================="
echo "  Step 1: 初始化管理员账户"
echo "=========================================="
echo ""
echo "1. 在浏览器中打开: http://<服务器IP>:${DIFY_WEB_PORT}/install"
echo "2. 设置管理员邮箱和密码"
echo "3. 完成初始化设置"
echo ""
echo "=========================================="
echo "  Step 2: 添加 Ollama 模型提供商"
echo "=========================================="
echo ""
echo '1. 登录后，点击右上角头像 → "设置"'
echo '2. 在左侧菜单中选择 "模型提供商"'
echo '3. 找到 "Ollama" 并点击添加'
echo '4. 填写以下配置:'
echo ""
echo "   ┌────────────────────────────────────────────┐"
echo "   │  模型名称:  ${OLLAMA_MODEL}                │"
echo "   │  基础URL:   http://localhost:11434          │"
echo "   │  (如果 Dify 和 Ollama 在不同机器上，       │"
echo "   │   使用 Ollama 所在机器的实际 IP)            │"
echo "   │  模型类型:  LLM (大语言模型)                │"
echo "   │  支持Vision: ✅ 是                          │"
echo "   │  上下文长度: 32768                          │"
echo "   │  最大Token:  8192                           │"
echo "   └────────────────────────────────────────────┘"
echo ""
echo '5. 点击 "保存" 完成模型配置'
echo ""
echo "=========================================="
echo "  Step 3: 创建面部健康分析应用"
echo "=========================================="
echo ""
echo '1. 回到首页，点击 "创建应用"'
echo '2. 选择 "从空白创建"'
echo '3. 应用类型选择 "聊天助手"'
echo '4. 应用名称: "AI面部健康分析助手"'
echo '5. 描述: "上传面部照片，AI为您分析健康状况并提供建议"'
echo ""
echo "=========================================="
echo "  Step 4: 配置应用提示词和模型"
echo "=========================================="
echo ""
echo '1. 进入应用编排页面'
echo '2. 选择模型: Ollama → '${OLLAMA_MODEL}''
echo '3. 在 "提示词" 区域填入以下系统提示词:'
echo ""
echo "────────────── 系统提示词（复制以下内容）──────────────"
cat << 'PROMPT'

你是一位专业的AI健康分析助手，擅长通过面部特征分析来评估用户的健康状况。

## 你的能力：
1. **面部特征分析**：观察面部肤色、气色、眼睛、嘴唇等特征
2. **健康状况评估**：基于面部观察提供初步健康参考
3. **生活建议**：提供饮食、作息、运动等方面的建议

## 工作流程：
1. 用户上传面部照片后，首先仔细观察照片中的面部特征
2. 从以下维度进行分析：
   - **肤色与气色**：面部整体肤色是否均匀、有无暗沉或发黄
   - **眼部状态**：是否有黑眼圈、眼袋、眼白是否清澈
   - **嘴唇颜色**：唇色是否红润、有无干裂
   - **面部浮肿**：是否有水肿迹象
   - **皮肤状态**：是否有痤疮、色斑、皱纹等
3. 给出综合健康评估和改善建议

## 输出格式：
请按以下结构输出分析结果：

### 📋 面部特征观察
- （列出观察到的主要面部特征）

### 🔍 健康状况分析
- （基于面部特征的健康分析）

### 💡 改善建议
1. **饮食建议**：...
2. **作息建议**：...  
3. **运动建议**：...
4. **其他建议**：...

### ⚠️ 重要提示
AI面部分析仅供参考，不能替代专业医学诊断。如有健康问题，请及时就医。

## 注意事项：
- 始终保持专业、客观的分析态度
- 不做医疗诊断，只提供健康参考和生活建议
- 使用温和、关心的语气与用户交流
- 如果照片不清楚或不是面部照片，礼貌提醒用户重新上传

PROMPT
echo ""
